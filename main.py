def svg_to_png(svg_content: str):
    """
      –ö–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—è svg-—Ñ–∞–π–ª–∞ –≤ png —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —É—Ç–∏–ª–∏—Ç—ã rsvg-convert

      Args:
          svg_content (str): svg-—Ñ–∞–π–ª

      Returns:
          io.BytesIO: png-–∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ, —Å–æ—Ö—Ä–∞–Ω–µ–Ω–Ω–æ–µ –≤ –±—É—Ñ–µ—Ä–µ.
      """
    try:
        process = subprocess.Popen(
            ["rsvg-convert", "-f", "png"],  # –∫–æ–º–∞–Ω–¥–∞ –∏ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã
            stdin=subprocess.PIPE,  # –ø–µ—Ä–µ–¥–∞—á–∞ –¥–∞–Ω–Ω—ã—Ö —á–µ—Ä–µ–∑ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –≤–≤–æ–¥
            stdout=subprocess.PIPE,  # –ø–æ–ª—É—á–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö —á–µ—Ä–µ–∑ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –≤—ã–≤–æ–¥
            stderr=subprocess.PIPE  # —Å–±–æ—Ä –æ—à–∏–±–æ–∫ —á–µ—Ä–µ–∑ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –ø–æ—Ç–æ–∫ –æ—à–∏–±–æ–∫
            )  # –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ —É—Ç–∏–ª–∏—Ç—ã rsvg-convert –∏–∑ –±–∏–±–ª–∏–æ—Ç–µ–∫–∏ librsvg —Å –ø–æ–º–æ—â—å—é subprocess, —Ç.–∫. –±–∏–±–ª–∏–æ—Ç–µ–∫–∞
        # –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ –Ω–µ –ø—Ä–µ–¥–Ω–∞–∑–Ω–∞—á–µ–Ω–∞ –¥–ª—è python –∏ –Ω–∞–ø—Ä—è–º—É—é –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∞ –±—ã—Ç—å –Ω–µ –º–æ–∂–µ—Ç

        png_data, error = process.communicate(
            input=svg_content.encode("utf-8"))  # –ø–æ–ª—É—á–µ–Ω–∏–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è png –∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏
        # –æ–± –æ—à–∏–±–∫–∞—Ö

        if process.returncode != 0:
            raise RuntimeError(f"–û—à–∏–±–∫–∞ rsvg-convert: {error.decode('utf-8')}")  # –æ—à–∏–±–∫–∞ –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏

        buffer = io.BytesIO(png_data)  # —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –≤ –±—É—Ñ–µ—Ä–µ (—á—Ç–æ–±—ã –Ω–µ –∑–∞–ø–∏—Å—ã–≤–∞—Ç—å —Ñ–∞–π–ª –Ω–∞ –∫–æ–º–ø—å—é—Ç–µ—Ä–µ)
        buffer.seek(0)  # –ø—Ä–æ—á—Ç–µ–Ω–∏–µ —Ñ–∞–π–ª–∞ —Å –ø–µ—Ä–≤–æ–≥–æ —Å–∏–º–≤–æ–ª–∞
        return buffer  # –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –≤ –±—É—Ñ–µ—Ä–µ

    except Exception as e:
        logging.error(f"–û—à–∏–±–∫–∞ –≤ svg_to_png: {str(e)}")  # –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ –æ—à–∏–±–∫–∏
        raise


